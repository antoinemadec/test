// every line of code that can actually run needs to be inside a class
//
// when we declare a public class, we must declare it inside a file with the
// same name (Main.java)
//
//
// System:
// is a pre-defined class that Java provides us and it holds some useful
// methods and variables.
//
// out:
// is a static variable within System that represents the output of your
// program (stdout).
//
// println:
// is a method of out that can be used to print a line.
public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}
